// Function to merge and update
const updateArabicTranslation = () => {
    const filePath = path.join(process.cwd(), 'messages/ar.json');
    const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    // Merge specific translations we defined
    Object.keys(arCards).forEach(key => {
        if (content.HouseOfCardsPage.cards[key]) {
             content.HouseOfCardsPage.cards[key] = arCards[key];
        } else {
             content.HouseOfCardsPage.cards[key] = arCards[key];
        }
    });

    Object.keys(additionalArCards).forEach(key => {
         if (content.HouseOfCardsPage.cards[key]) {
             // For simplified ones, we might need to be careful not to overwrite full data with partial if specific fields are missing
             // But here we are just adding what we translated.
             content.HouseOfCardsPage.cards[key] = { ...content.HouseOfCardsPage.cards[key], ...additionalArCards[key] };
         }
    });

    fs.writeFileSync(filePath, JSON.stringify(content, null, 4), 'utf8');
    console.log("Arabic translations updated.");
};

